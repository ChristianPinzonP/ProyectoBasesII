  CREATE OR REPLACE EDITIONABLE TRIGGER "ROOT2"."TRG_NO_DOCENTE_TITULAR" 
BEFORE INSERT OR UPDATE ON "ROOT2"."DOCENTE_GRUPO"
FOR EACH ROW
DECLARE
    v_titular NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_titular
    FROM "ROOT2"."GRUPO"
    WHERE "ID_GRUPO" = :NEW."ID_GRUPO" 
    AND "ID_DOCENTE_TITULAR" = :NEW."ID_DOCENTE";

    IF v_titular > 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'El docente ya es titular de este grupo');
    END IF;
END;

